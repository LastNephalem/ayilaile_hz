package com.example.lucifer.ayilaile_hz.bean;

public class LifeItem {

    private String name;

    private int imageId;

    public LifeItem(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}